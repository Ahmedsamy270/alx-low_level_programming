#include "3-calc.h"

/**
 * op_add - as a function to add two integers
 * @a: is the first integer
 * @b: is the second integer
 * Return: The Result Of Function
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - is a function to subtract to integers
 * @a: is the first integer
 * @b: is the second integer
 * Return: The Result Of Function
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - is a function to multibly two integers
 * @a: is the first integer
 * @b: is the second integer
 * Return: The Result Of Function
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - is a function to divition two integers
 * @a: is the first integer
 * @b: is the second integer
 * Return: The Result Of Function
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - is a function to get the module of two integers
 * @a: is the first integer
 * @b: is the second integer
 * Return: The Result Of Function
 */

int op_mod(int a, int b)
{
	return (a % b);
}
